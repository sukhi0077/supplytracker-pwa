-- Insert mappings for item names that resolve to an item and aren't present yet.
insert into public.ksef_mappings (ksef_item_name, item_id, supplier_id, pack_size)
select m.ksef_item_name, i.id, null, 1
from _map_import m
join public.items i on lower(i.name) = lower(m.item_name)
where not exists (
  select 1 from public.ksef_mappings k
  where k.ksef_item_name = m.ksef_item_name and k.supplier_id is null
);

-- How many were inserted / already existed:
select
  (select count(*) from _map_import) as source_pairs,
  (select count(*) from _map_import m join public.items i on lower(i.name)=lower(m.item_name)) as matched_to_item,
  (select count(*) from public.ksef_mappings) as total_mappings_now;

-- Item names that did NOT match any Supabase item (fix the name, then re-run):
select distinct m.item_name
from _map_import m
left join public.items i on lower(i.name) = lower(m.item_name)
where i.id is null
order by m.item_name;
